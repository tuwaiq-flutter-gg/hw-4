main() {
/*
1. `9 == 9`
 */
  print(true);
  print(9 == 9);
/*:
 2. `9 != 9`
 */
  print(false);
  print(9 != 9);
/*: 
 3. `47 > 90`
 */
  print(false);
  print(47 > 90);
/*:
 4. `47 < 90`
 */
  print(true);
  print(47 < 90);
/*:
 5. `4 <= 4`
 */
  print(true);
  print(4 <= 4);
/*:
 6. `4 >= 5`
 */
  print(false);
  print(4 >= 5);
/*:
 7. `(47 > 90) && (47 < 90)`
 */

  print(false);
  print((47 > 90) && (47 < 90));
/*:
 8. `(47 > 90) || (47 < 90)`
 */
  print(true);
  print((47 > 90) || (47 < 90));

/*:
 9. `!true`
 */
  print(false);
  print(!true);
}
